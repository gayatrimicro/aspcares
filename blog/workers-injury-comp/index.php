                                        <p class="product_desc">
                                       When you experience an on-the-job injury, you may not know what to expect. While the rules vary from state to state, certain features are common to almost all. Worker’s injury compensation is, first and foremost, a form of insurance that certain employers are required to carry for their workers. It is designed to cover the cost of a portion of the employee’s lost wages, medical expenses, and prescription medications related to the injury or occupational disease.</p>
                                         <p class="product_desc">

                                         <b class="find"> What does Worker’s Injury Compensation Cover?</b><br>
                                         Worker’s compensation, sometimes referred to as worker’s compensation insurance, typically covers expenses related to the diagnosis and treatment of injuries sustained on the job, including chronic conditions caused by repetitive movements such as carpal tunnel syndrome (an injury to the wrist/hand often related to typing). To qualify, the person applying must show that the injury or disease is related to their work. This process varies from state to state.
                                         </p>
<p class="product_desc">   <b class="find">

                                         How long does it usually take to get worker’s compensation?</b><br>


If your claim is approved, it generally takes less than two weeks for you to receive a check from the time your employer was notified of the injury and or qualifying employment related condition.</p>

<p class="product_desc"><b class="find">

Is worker’s compensation paid weekly?</b><br>
The benefits are usually paid weekly, bi-weekly, or monthly depending on the state you are employed in.</p>

 <p class="product_desc">
   
  <b class="find"> What if my claim is denied?</b><br>
Each state has their own process for appeals. Even if your claim is denied, there are usually options for injured workers such as a direct appeal to your state’s worker’s compensation. Your claim may be denied for several reasons including:</p>
<p class="product_desc find">
• Not filing your claim on time<br>
• Not reporting the injury promptly<br>
• Not enough medical evidence of an injury<br>
• No proof the injury or condition was work related<br>
• Not receiving medical attention for the injury

 </p>
                                    
 <p class="product_desc ">
  <b class="find"> Can I get worker’s compensation if the employer disputes my claim?</b><br>
If your employer states that your injury is not work related, was the result of horseplay, or occurred elsewhere, your claim may be denied. Because it is a form of insurance, your employer’s insurance rates may be increased if you are approved.
