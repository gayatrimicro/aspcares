<h2 class="find htwo left">What is a Letter of Protection?</h2></p>
<p class="product_desc">
  A letter of protection, or LOP, is a formal contract between yourself, your attorney, and a medical provider such as a physician or pharmacy. In a nutshell, a letter of protection promises the provider they will receive payment for services rendered once your case is settled.</p>
  










 
