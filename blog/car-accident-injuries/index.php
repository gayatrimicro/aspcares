<p class="product_desc"><h2 class="find htwo left">
How can I Get Prescriptions After a Car Wreck with No Out of Pocket?</h2></p>

<p class="product_desc">
  Getting the care you need however, is not really something that can wait. Even if a car wreck seems relatively minor, you might have underlying auto accident injuries that may require treatment.  
</p>


<p class="product_desc"><b class="find">But how do I pay for it? </b><br>
There are several possible routes you can take. If you have medical insurance, for instance, you can use it to pay for medical care as well as any necessary prescriptions. If not, you still might have other options. These may include:</p>

<p class="product_desc"><b class="find">MedPay (Medical Payment to Others Insurance)</b> <br>
Some insurance companies offer an optional form of coverage known as medpay. MedPay may cover a certain amount of your medical and or prescription medication costs for the accident, or those of any passengers who may also have been injured in the wreck, regardless of who was actually at fault for the accident. The limits vary from carrier to carrier. </p>

<p class="product_desc"><b class="find">No Fault Insurance</b> <br>
If you have no fault insurance, you may be able to file a claim so that the auto insurance carrier pays at least some of the cost of your medical care, including any necessary medications related to your car wreck injuries.  No fault coverage is not available in all states. In states that do allow no fault insurance, coverage limits can vary widely, so be sure to check your policy. </p> 

<p class="product_desc"><b class="find">State Medicaid</b><br>
In some states, if you are injured in an accident (or lose your job due to injuries or illness), you may qualify for state Medicaid coverage. If you need care and do not have insurance or a way to pay, you might be able to receive care via your state’s Medicaid program. Depending on the way your state manages its Medicaid program, you may be able to have your future medical expenses and medication costs covered. In some states, Medicaid may also cover recent past medical expenses.
</p>

  <p class="product_desc"><b class="find">Will Medical Providers and Pharmacies Work with Me?</b><br>
Sometimes medical providers will wait for payment through direct arrangements too. In such cases you may have to sign a personal injury lien, which is essentially a promise to pay once the case is settled. </p>




 
