If you are pregnant and test positive for HIV, you should talk to your doctor about starting treatment as early as possible, even if you do not have symptoms. (Many people do not experience symptoms for as much as a decade or more after becoming infected.) </p>
<p class="product_desc">
You can still transmit the virus to your unborn child if you are infected but not showing any symptoms. 

