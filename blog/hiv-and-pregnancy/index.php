    It is recommended that you have an HIV test at your first prenatal visit, during your third trimester, and after delivery. Even if you have declined a previous HIV test, you can still ask for one at any time. You should also ask your partner to be tested at least once. </p>
    <p class="product_desc"> 
And, if you do have unprotected sex or share needles at some point during your pregnancy, you should be tested for HIV again. 

</p>


<p class="product_desc"><b class="find">Why should I be tested for HIV more than once? </b></p>
<p class="product_desc">If you have been tested and it came out negative, there is a chance that you were tested in what is known as the window period. The window period is a length of time in which your body has not created enough antibodies for the test to detect, even though you are actually infected. </p>

<p class="product_desc">
If you think you have been exposed to HIV recently, you should talk to your healthcare provider about follow-up testing. Fortunately, some of the newer tests can dramatically reduce the window period from weeks to as little as 11 days after infection. Click here to learn more about the window period and HIV testing. <a href="../hiv-treatment-and-prevention/" class="redunder">Click here.</a>


</p>




<p class="product_desc"><b class="find">What if I cannot afford HIV testing?</b><br>
Testing is simple, fast, and painless. If you cannot afford an HIV test, low cost and or no-cost tests are available. Your local health department, for instance, may offer free testing. Many Planned Parenthood locations also offer either low-cost or free HIV tests as well as other services for those who cannot afford them. </p>
<p class="product_desc">
Additionally, many substance abuse centers, local health departments, community clinics, and hospitals either offer free or low-cost testing, and or can advise you where to go so you can be tested. You can also use the low-cost/free testing site locator provided by the Center for Disease Control by <a href="https://gettested.cdc.gov/" target="_blank" class="redunder" rel="nofollow">clicking here.</a>  
 
</p>
