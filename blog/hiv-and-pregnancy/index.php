Women are at greater risk of becoming infected with HIV during vaginal intercourse than their male partners are. If your partner is HIV positive, you can drastically reduce your risk of becoming infected by using pre-exposure prophylaxis, or PrEP. You should know that stopping the medication or skipping doses makes the medication much less effective.</p>
