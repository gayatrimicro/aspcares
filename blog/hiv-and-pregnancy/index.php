 <p class="product_desc">  
 Just because you are HIV positive does not mean you cannot become pregnant. Nor does it mean that you can’t give birth to a healthy, HIV-negative baby. You should understand though, that there is some risk associated with pregnancy when you are HIV positive, because it is possible to transmit the human immunodeficiency virus (HIV) to your baby during pregnancy, childbirth, or when breastfeeding. </p>
 <p class="product_desc">  
According to the Center for Disease Control, if you are HIV positive, you can reduce the risk of transmitting the virus to your baby to as low as 1%, by taking HIV-related medications exactly as prescribed by your healthcare provider. 

 </p>
 
                                    
 <p class="product_desc">
  <h2 class="find htwo">How Can I Lower My Risk of Passing HIV to My Unborn Baby?</h2></p>
  <p class="product_desc">
If you are pregnant and have HIV, the best way to reduce any chance of passing the infection on to your baby is to remain as healthy as possible. Your healthcare provider can advise you concerning ways to protect yourself and your unborn child from HIV-related complications. Click here to learn more about <a href="../hiv-treatment-plan/" class="redunder">HIV treatment medications.</a> 

</p>


<p class="product_desc">
  <h2 class="find htwo">90% of Babies Born to HIV Positive Mothers Do Not Have HIV</h2></p>
  <p class="product_desc">
According to the Office on Women’s Health (U.S. Department of Health and Human Services), the number of babies born with HIV has fallen by 90% since the mid-1990s. This is believed to be directly related to the use of antiretroviral medications alongside other HIV-positive pregnancy treatment protocols. 
</p>


<p class="product_desc">
  <h2 class="find htwo">If You Are Pregnant or Considering Becoming Pregnant, Get Tested for HIV</h2></p>
 <p class="product_desc">Knowing your HIV status can help you make informed healthcare decisions. If you are considering becoming, or are already pregnant, and you don’t know your HIV status, you should be tested as soon as possible. <a href="../hiv-diagnosis/" class="redunder">Click here.</a>  </p>
 <p class="product_desc">
Even if you are already pregnant, you can become infected if you have unprotected anal, oral, or vaginal sex, share contaminated needles, or share “works” (injection equipment including water and cotton), piercing, and or tattooing needles.
