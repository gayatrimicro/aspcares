<p class="product_desc">  
Even if your partner is HIV positive, there are other options to help you become pregnant. You might consider invitro fertilization using a process known as sperm washing. Sperm washing is used to remove the virus from the semen of HIV positive males before artificially inseminating the female. </p>
<p class="product_desc">
The <a href="https://www.cdc.gov/mmwr/volumes/66/wr/mm6621a2.htm" target="_blank" class="redunder" rel="nofollow">Center for Disease Control (CDC)</a> has determined the process is both safe and effective for couples wishing to become pregnant when the male partner has HIV and the female is HIV negative. Your doctor can explain the process to you, as well answering any questions you may have. 

</p>


<p class="product_desc">
 <b class="find">Preventing Perinatal HIV Transmission (Mother to Child HIV Infection) </b><br>
 Healthy mothers are more likely to give birth to healthy babies. <a href="../hiv-treatment-and-prevention/" class="redunder">HIV medications</a> can reduce your HIV viral load (the amount of virus within your body).</p>
 <p class="product_desc">
According to the National Institute of Health, higher levels of maternal viral load increase the risk of passing HIV to the child during pregnancy, childbirth, or breastfeeding. The lower the load, the less risk that your child will become infected with HIV.
</p>



 
