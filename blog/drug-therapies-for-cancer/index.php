<p class="product_desc"><h2 class="find left htwo">
Helping Killer T-Cells to Find and Destroy Cancer with New Therapies for Cancer</h2></p>
<p class="product_desc">While one might think that cancer cells are obvious within the body, they are not. 
Cancer can be very sneaky. For example, cancer cells can hide using pathways such as PD-1. The PD-1 pathway acts like an off switch. Healthy cells use it to tell the immune system not to attack them. Cancer cells use it too, making it harder for T cells (which help identify and kill invaders and rogue cancer cells) to find them.
</p>



<p class="product_desc"><h2 class="find left htwo">It’s No Game: Playing Hide and Seek with Cancer Cells</h2></p>
<p class="product_desc">
This kind of cancer treatment uses biomarkers to help the body identify cancer cells. (Biomarkers are processes or substances, such as a molecule secreted by a tumor which are unique to a given cell type.) </p>
<p class="product_desc">
For example, the biomarker used by Keytruda is known as microsatellite instability-high (MSI-H) or mismatch repair deficient (dMMR). It is related to certain kinds of mutations within cancer cells. Normally, when a cell divides, it does so perfectly, creating healthy daughter cells. In some cases, though, the DNA is damaged in precise ways that can be targeted.</p>
<p class="product_desc">
When cancer cells can’t use cellular camouflage to hide, they are less likely to grow and spread. Keytruda has been approved for 11 different kinds of cancers that carry a specific biomarker, no matter their location. 
</p>

<p class="product_desc"><h2 class="find left htwo">
Other Revolutionary Ideas That are Changing Cancer Drug Therapies</h2></p>
<p class="product_desc">
  Other ideas, such as keeping cancer cells alive, but in check, are also being explored. Some researchers are looking into the possibility of avoiding high-dose chemotherapies. While not yet approved for human trials (studies are being done on mice), the strategy is more about mitigation than annihilating cancer cells.</p>

  <p class="product_desc">
  Researchers are looking into the concept of holding cancers at bay, improving the patient’s quality of life, yet still decreasing the number of cancer cells by using a diminishing dose. This technique is known as adaptive therapy. </p>

<p class="product_desc">
Adaptive therapy focuses on stabilizing tumors with the lowest possible effective dose. Dosages are fine-tuned so that they shrink tumors but do as little damage to healthy cells as possible. <a href="../october-is-breast-cancer-awareness-month/" class="redunder"> Click here.</a></p>


<p class="product_desc"><b class="find">New Hope for Cancer Patients </b><br>
