                                        <p class="product_desc">
                                         Each year the FDA approves promising new drug therapies for cancer. It now appears that some of the most promising cancer treatment avenues, may actually lie within the bodies of the patients themselves. 

                                            </p>



     

<p class="product_desc">
<h2 class="find htwo left">Next Generation Non-Toxic Drug Therapies for Cancer</h2></p>
<p class="product_desc">Known as immunotherapy or targeted gene therapies, these kinds of treatments harness naturally occurring processes to create personalized, less toxic treatments, which are potentially much safer for healthy cells. The possible benefits of this kind of treatment are hard to overstate. </p>

<p class="product_desc">
<h2 class="find htwo left">
Targeted Cancer Treatments</h2></p>
<p class="product_desc">Like the development of chemotherapy in the 1940’s, immunotherapy and targeted gene therapies could be real game changers. Immunotherapy, especially when combined with other targeted medications, has the potential to forever alter the landscape of cancer treatments. </p>
<p class="product_desc">
This is especially true if it is partnered with gene specific cancer therapies. Both of which can be used with, or without, traditional cancer treatments such as chemotherapy and radiation.
</p>


  <p class="product_desc"><b class="find">Drug Therapies for Cancer That Can Hit Tiny Hot Zones </b><br>
  Gene targeting cancer treatments work by locking onto specific genes and or proteins within the cancer cells themselves. According to <a href="https://www.ncbi.nlm.nih.gov/pubmed/9406237" target="_blank" rel="nofollow" class="redunder">research</a> in the US National Library of Medicine National Institutes of Health, gene targeting works within the microenvironment of the tumor. In other words, the treatment is directed within an almost unbelievably narrow range- at the cellular level.</p>

<p class="product_desc">
  <b class="find">
Potentially Fewer Side Effects  </b><br>

These kinds of treatments are so precisely targeted they can reduce the risk of some kinds of unwanted side effects. One of the reasons that cancer patients experience symptoms such as hair loss, nausea and vomiting is because radiation and many cancer drugs (called antineoplastics) are toxic to cancer cells. </p>
<p class="product_desc">
Because cancer cells arise from the human body and share many of the same characteristics of normal cells, healthy tissues can also be damaged in the process. </p>
<p class="product_desc">
That is not to say that these new forms of treatment are not without risk. Like all treatment forms and drug therapies for cancer, they have the potential to cause unwanted side effects.



</p>

<p class="product_desc">
<h2 class="find htwo left">Immune Based Cancer Treatments</h2></p>
<p class="product_desc">Immune based therapies for instance, may trigger excessive immune responses, potentially causing symptoms that are similar to disorders such as Lupus. Side effects can range from mild to severe, even becoming life-threatening.  

</p>

 <p class="product_desc"><b class="find">                                                   
Keytruda</b><br>
The recently approved medication <a href="https://www.keytruda.com/" target="_blank" class="redunder" rel="nofollow">Keytruda</a> (pembrolizumab), which fights certain solid cancerous tumors is an example an advanced form of immunotherapy. Keytruda has been approved by the FDA to treat cancers such as melanoma which have metalized (spread) to other parts of the body, and cannot be removed surgically. </p>
<p class="product_desc">
Keytruda is not a radiation or traditional chemotherapy agent. Instead, it works with the immune system to fight cancer where it hides. Approved in late 2017, this groundbreaking drug therapy for cancer is the first of its kind. </p>
<p class="product_desc">
Cancer drug therapies in the past have been based on where the tumor originated. This  type of medication is different in that it attacks cancer cells, regardless of where in the body they are located- or where they started. 


</p>










 
