<p class="product_desc"><h2 class="find htwo left">
HIV Testing: Can You Get a False Positive? </h2>
</p>
<p class="product_desc">
It is possible, but extremely rare to receive a false positive for an HIV test. It is far more likely for someone to receive a false negative because of the limitations of current testing methods. A false negative means that is tested and even though they are infected, the tests show them to be HIV free. </p>


<p class="product_desc"><b class="find">How does a false negative HIV test occur?</b><br>
HIV tests are designed to detect the specific antibodies your body produces when infected with HIV. There is an interval of time, known as the “window period” in which the body simply has not had time to produce enough antibodies to be detected by the tests. </p>

<p class="product_desc">Because the tests cannot detect antibodies that your body has not yet produced, some people may believe that they are HIV negative, when in fact they are HIV positive. This is why repeated, regular testing is so critical to diagnosing HIV.</p> 
<p class="product_desc">A false positive, on the other hand, occurs when someone who is not HIV positive is identified as having the virus. False positives occur in about three out of every 100,000 people tested. </p>
<p class="product_desc">When false positives occur, they are usually the result of the test detecting antibodies that are very similar to the ones created for HIV. Although rate, certain autoimmune disorders, such as lupus can cause a false positive. </p>
</p>

<p class="product_desc"><b class="find">There is no Single HIV Test</b><br>
It is important to note that there is no single HIV test to diagnose HIV. Serological tests (which detect the presence of antibodies), performed in a specific order and combination however, can detect HIV with great accuracy, provided there has been enough time for the body to create them.</p>
<p class="product_desc"><b class="find">What is a Baseline HIV Evaluation? </b><br>
A baseline evaluation is an initial examination. For those newly diagnosed as HIV positive, it typically includes laboratory blood work, other medical tests (such as X rays), a complete physical examination, taking a detailed medical history, and a consultation. </p>
  <p class="product_desc"><b class="find">What Kinds of Lab Tests are Part of a HIV Baseline Evaluation? </b><br>
  A baseline HIV diagnosis evaluation may include a series of lab tests such as:</p>
  <p class="product_desc">

• HIV retesting (If the documentation for the original test is not available)<br>
• Plasma HIV RNA test (to determine the patient’s current viral load)<br>
• T cell count (also known as CD4 T lymphocyte count)<br>
• Serology tests for hepatitis A, B and C<br>
• Tests for other sexually transmitted diseases (STDs)<br>
• Fasting glucose (blood sugar levels)<br>
• Genotype resistance testing <br>
• Complete blood count and chemistry profile<br>
</p>


