Medicare is a federal health insurance program for people who are over the age of 65. It also covers some others, such as younger people who are disabled, or who have end stage renal (kidney) disease which requires kidney dialysis or a kidney transplant. Medicare also covers certain HIV expenses, such as those associated with the cost of HIV medications. You can learn more about covered services <a href="https://www.medicare.gov/Pubs/pdf/11109-Your-Guide-to-Medicare-Prescrip-Drug-Cov.pdf" target="_blank" class="redunder">here.</a>  
