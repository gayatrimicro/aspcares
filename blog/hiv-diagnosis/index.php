                                         <b class="find">Make an Appointment with Your Healthcare Provider </b><br>
                                         The first thing you need to do after receiving a HIV diagnosis is to make an appointment with your healthcare provider. Even if you have not developed symptoms of HIV, you can still transmit the virus to others. Ignoring you HIV diagnosis will not make it go away, and early treatment is critical for managing the disease.
                                         </p>

                                         <p class="product_desc">


<b class="find">Why Early Treatment is Important </b><br>
Your healthcare provider can provide you with medications, supportive care, critical information and <a href="../hiv-treatment-and-prevention/" class="redunder">other resources</a> to help you fight the disease. </p>
 <p class="product_desc">If you do not currently have HIV symptoms, your healthcare provider can evaluate you to determine when to start treatment, as well as which HIV medications should be part of your treatment regime. </p>

