 <p class="product_desc">  
  <h2 class="find htwo left">HIV Diagnosis Treatment</h2></p>
  <p class="product_desc">
The bulk of HIV treatment centers around medications known as antivirals, or ARTs. Patients on antiviral therapy typically take a combination of medications which work to reduce the viral load (the amount of the active virus in your body) and <a href="../understanding-hiv-medications/" class="redunder">slow the progression the disease.</a>

 </p>
                                    
