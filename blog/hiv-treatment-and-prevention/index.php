<p class="product_desc"><b class="find">
What are the signs and symptoms of HIV?
</b><br>
The symptoms of an HIV infection can vary widely, depending on the stage of the disease. Many HIV positive people are not aware that they are infected. They may or may not, experience flu-like symptoms which can easily be missed or mistaken for other kinds of infections in the first few weeks after they are infected.</p> 

<p class="product_desc">Early symptoms can occur within two weeks to two months of infection. It should be noted, many of these symptoms can be caused by other common health conditions and illnesses. The only way to determine if someone is HIV positive, or infected with HIV, is to be tested. HIV can also be transmitted from mother to child during pregnancy, labor or birth. 
</p>

<p class="product_desc"><b class="find">
Early Symptoms of HIV Infection can Include:</b><br>

• Headaches<br>
• Fatigue (tiredness)<br>
• Swollen lymph glands<br>
• Fever<br>
• Sore throat<br>
• Skin rashes<br>
• Joint and or muscle pain and other flu-like symptoms<br>
• Ulcers in the mouth<br>
• Ulcers on the genitals<br>
• Diarrhea<br>
• Night sweats


</p>


<p class="product_desc"><b class="find">
How is HIV Transmitted?</b><br>
HIV can be transmitted by sharing contaminated needles, having anal, vaginal or oral sex with someone who is HIV positive, even if they do not have symptoms of the disease. </p>


<p class="product_desc"><b class="find">What are the Risk Factors for HIV?</b><br>
<p class="product_desc">HIV can affect anyone, regardless of their race, gender, or sexual orientation. Risk factors for HIV include:</p>
<p class="product_desc">
• Having unprotected sex (vaginal, oral or anal) with a single or multiple partners<br>
• Sharing contaminated needles or other injecting equipment for drug use<br>
• Having another form of sexually transmitted disease (STD)<br>
• Receiving unsafe blood products or non-sterile piercings or tattoos<br>
• Accidental needle sticks (usually among healthcare workers)
</p>

 <p class="product_desc"><b class="find">Is HIV a Disease that Only Affects People in the LGBT Community? </b><br>

No. Anyone, regardless of sexual orientation, socioeconomic level, race or ethnicity can become infected with HIV. 
 </p>


 <p class="product_desc"><b class="find">How is HIV Diagnosed? </b><br>

HIV testing requires a blood test. There is no one single blood test that can detect HIV, but HIV tests can be done in combination to provide a diagnosis. When properly performed, HIV testing can determine if someone is infected with a high degree of accuracy. 
 </p>


 <p class="product_desc"><b class="find">How Can I Help Prevent the Spread of HIV?</b></p>
<p class="product_desc">
• Always use safe sex practices such as using a condom <br>
• Do not share needles<br>
• Get HIV testing and preventative counseling<br>
• Use prescribed antiviral medications (Truvada)

