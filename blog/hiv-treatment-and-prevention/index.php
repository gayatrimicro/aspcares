                                        <p class="product_desc">
                                       HIV, or the human immunodeficiency virus, is one which attacks the human immune system. HIV treatment and prevention are critical steps in stopping the spread of this disease. Left untreated, the HIV virus can cause a serious, even life-threatening disorder known as AIDS, or Acquired Immune Deficiency Syndrome.</p> 

<p class="product_desc">To understand why HIV is so unusual, you first need to know how your immune system works. Your immune system is the part of your body that protects you from infections caused by viruses and bacteria. </p>
<p class="product_desc">When someone is immunocompromised (meaning that their immune system is weakened), they are especially vulnerable to what might otherwise be only a minor infection the body could fight off. 
</p>
  
                                         <p class="product_desc">

                                         <h2 class="find htwo left">Why HIV is Different from Other Viruses</h2> </p>
                                         <p class="product_desc">HIV is different from other kinds of viruses, because the HIV virus seeks out and kills special cells known as T cells (also called CD4 helper cells). T cells are a type of lymphocyte, or white blood cell. They form a critical line of defense against diseases and infections. 
                                         </p>

                                         <p class="product_desc">


<b class="find">How T Cells Protect Your Body </b><br>
 T cells are very specialized. They have very specific functions such as identifying foreign invaders and helping other cells, called B cells. B cells create targeted antibodies to locate and kill bacteria and viruses. </p>
 <p class="product_desc">By attacking the T cells, HIV disrupts the immune system, making it much harder for people who are HIV positive to fight back against infections. <a href="../hiv-diagnosis/" class="redunder">Click here.</a></p>
 <p class="product_desc">Additionally, HIV is a retrovirus. This means that it has an enzyme called reverse transcriptase that allows it to insert its own DNA into the host cells. It uses this to force T cells to make copies of the virus, spreading more of itself throughout the body.</p> 
 <p class="product_desc">HIV also has a high rate of mutation, which means that there are many different strains of the disease. Because there are so many variants, a single vaccine that could protect people from HIV is very, very difficult to create. This is one reason why why early HIV treatment and prevention are so important. 

</p>

 
 <p class="product_desc">  
  <h2 class="find htwo">How Long Does It Take for Untreated HIV to Cause AIDS? </h2></p>
  <p class="product_desc">
HIV is like a viral time-bomb. Someone can be infected with it and not know they are HIV positive. Some people may carry the virus for a decade or more before symptoms begin to show. The average time between HIV infection and developing AIDS is usually within 5 to 10 years. That timeline is not set though; it can be much shorter, or even longer, depending on the patient.  

 </p>
                                    
 <p class="product_desc">
  <b class="find">Antivirals: HIV treatment and Prevention</b><br>
HIV treatment usually centers around antiviral therapies, or ART. Antivirals are designed to lower the viral “load” in the infected person’s body. By reducing the number of virons (active copies of the virus) down to an undetectable level, HIV positive patients are less likely to develop the disease or pass it on to others. 
</p>
    
  <p class="product_desc"><h2 class="find htwo">HIV Treatment and Prevention FAQ’s</h2></p>

<p class="product_desc"><b class="find">How Many People are Living With HIV?</b><br>

As of 2016, the World health Organization estimates that well over 37 million people are HIV positive. Moreover, more than a million people die worldwide of HIV related causes each year. While HIV is treatable, most of these deaths occur in developing nations, where health resources are scarce. (According to the World Health Organization, over 95% of all HIV positive people live in developing countries.) </p>


<p class="product_desc"><b class="find">
Opportunistic Infections and HIV/AIDS</b><br>
By far, the greatest threat for those with HIV/AIDS is what are known as opportunistic infections, or OIs. Even with antivirals, HIV positive patients can be vulnerable to opportunistic infections. </p>
<p class="product_desc">In some cases, patients are not aware that they are HIV positive. In other cases, they are aware but untreated. In others still, treatment may lower, but not completely eliminate enough of the live virus in their body, rendering them infectious and a weakened immune system.</p>
<p class="product_desc">When HIV positive individuals are infected with certain opportunistic infections, they are considered to have crossed the threshold from being HIV positive to having developed AIDS, no matter how many T cells they have.</p> 
<p class="product_desc">A person is also considered to have developed AIDS if their T cell numbers drop below 200. (The normal range for T cells is between 500 and 1,500.) 
</p>

<p class="product_desc">
<b class="find">
Common Opportunistic Infections for HIV/AIDS Include:</b><br>
• Candida<br>
• Cryptococcosis<br>
• Invasive Cervical Cancer<br>
• Cytomegalovirus <br>
• Lymphoma<br>
• Tuberculosis<br>
• Pneumocystis cariniipneumonia (PCP)<br>
• Recurrent Pneumonia<br>
• Recurrent Salmonella septicemia <br>
• Progressive multifocal leukoencephalopathy<br>
• Toxoplasmosis of the brain<br>
• Wasting Syndrome related to HIV<br>
• Mycobacterium avium complex (MAC) 

</p>


