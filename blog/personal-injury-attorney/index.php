                            <h2>How it Works</h2><br>
                           
                            <p>Once a personal injury attorney has accepted your case, they will often write what is known as an LOP, or letter of protection. A form of promissory payment, it is basically a request for credit. A LOP allows you to see a seek medical attention while you wait for the legal matters to be worked out. </p><br>
                            <p>Typically, a letter of protection states that if the case is settled, or the court rules in your favor, the pharmacy and or medical providers will be paid out of that money. If, on the other hand, the case is dismissed, or you do not win your case in court, then you become responsible to pay for any services that have been rendered.</p>
                            <br>
                            <p>Additionally, a LOP can help to protect your credit, delaying any collection process for medically related services until any pending legal actions have been finalized. (The case is settled, dropped, dismissed, or you have won or lost your case in court.) Your personal injury attorney can explain the legal details of how the LOP process works.</p>
