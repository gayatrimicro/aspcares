                                        <p class="product_desc">
                                       While there is still no cure for hemophilia, and the condition can’t be ignored- it can be treated. By carefully managing hemophilia, people living with the disease can do just that: live.</p>


                                        
                                        
                                      
                                    
                                        <p class="product_desc"><b class="find">
                                           What is hemophilia?
                                            </b><br>
                                            Hemophilia is an inherited bleeding disorder that mainly affects males. It is caused by a missing coagulation (blood clotting) factor, usually factor VIII (hemophilia A), factor IX (hemophilia B), or factor XI (hemophilia C). 80-85% of all hemophilia cases are diagnosed as hemophilia A, making it the most common form of the disorder.
</p>

 <p class="product_desc">
Hemophilia, like other bleeding disorders, is admittedly challenging, but that does not mean someone can’t live a long and happy life.
When it comes to <a href="../all-about-hemophilia-replacement-therapy/" class="redunder">managing hemophilia,</a> the focus is always on controlling bleeding episodes and preventing future ones.</p>

 



                                             <p class="product_desc"><h2 class="find htwo">
                                           Managing Hemophilia Day-to-day
                                            </h2></p>
                                            <p class="product_desc">
                                            <a href="../hemophilia-medication-plan/" class="redunder">Hemophilia treatment</a> regimens can vary somewhat, depending on the severity and type of hemophilia. All treatment plans revolve around vigilant symptom monitoring, restorative care (such as treating a bleed when it occurs), and prophylactic (preventative) treatment.<br>
                                            This may involve infusions of missing clotting factors to treat a current bleed, prevent future bleeds, and or the use of supportive medications, including medications for pain management.</p>
<p class="product_desc">This is often done through a combination of doctor’s office visits and at home infusions. Younger children are sometimes treated with implanted devices to make injections easier. Properly managing hemophilia means that those living with the disorder have greater freedom to: travel, participate in more physical activities, pursue a wider range of professions, and live more fully than ever before.

                                            </p>


        <p class="product_desc"><b class="find">
                                           People with Hemophilia are More Likely to Become Obese
                                            </b><br>
                                            Just as it is in the overall population, obesity is an ongoing issue in the hemophilia community. Obesity rates have tripled since the 1980’s. According to a study by the Center for Disease Control (CDC) both children and teenagers living with bleeding disorders are almost twice as likely as their peers to be overweight.</p>
<p class="product_desc">Obesity places additional strain on joints already vulnerable to bleeding. It is critically important for people living with hemophilia to maintain a healthy weight. The best tools for managing weight for those with hemophilia are the same as those for people without the disorder, namely, diet and exercise.</p>                                      
                                                                               
                                            
 <p class="product_desc"><b class="find">
                                           Physical Activity for People Living with Hemophilia
                                            </b><br>
  Doctors used to recommend that those diagnosed with <a href="../hemophilia-diagnosis/" class="redunder">hemophilia</a> avoid physical activities due to a perceived higher risk of bleeding episodes. Later studies, however, showed that the increased risk for loss of bone density, combined with obesity and other related conditions, were actually more harmful than exercising on a regular basis.</p>
<p class="product_desc">Besides helping improve balance and coordination, remaining physically active improves circulation, fight depression, increases range of motion, and even helps to reduce pain levels. Your healthcare provider can help determine which activities are right for you, but generally speaking, swimming, non-contact sports, walking, and exercises that focus on conditioning are encouraged.
</p>




                                                </div>
                                                <div class="product_header_cell product_header_img">
                                                   
                                             <p class="product_desc"><b class="find">
                                           Use Proper Safety Gear and Precautions 
                                            </b><br>
                                          Be careful to use any relevant safety gear and talk to your healthcare provider about precautions, treatment options and or preventative measures you (or your child) should take before starting any form of physical activity.</p>
<p class="product_desc">You should also start slowly, paying attention to the severity of your symptoms and condition of your joints. Additionally, make sure to report any bleeding episodes to your healthcare provider and or seek immediate medical attention if required.</p>


 <p class="product_desc"><b class="find">
                                           Diet and Nutrition for Hemophilia and Other Bleeding Disorders
                                            </b><br>
                                              While there is no specific diet recommended for those with hemophilia or other bleeding disorder, a balanced diet rich fruits and vegetables, high fiber, lean proteins and healthy fats is typically advised. Your healthcare provider can advise you as to which supplements should be included or avoided, as well as information in portion control, recommended foods and ones which should be avoided.
                                            </p>


                                            <p class="product_desc"><b class="find">
                                          Talk to Your Healthcare Provider Before Taking Nutritional Supplements
                                            </b><br>
  Some supplements can help people with bleeding disorders, while others may increase the risk of a bleeding event. For example, although red blood cell production does involve vitamin E, high doses can greatly increase the risk of a bleed.</p>
<p class="product_desc">Other supplements such as garlic, ginger, flaxseed, fish oil, ginkgo biloba may also need to be avoided for similar reasons. You should always consult a qualified medical provider before taking any medication or supplement.
</p>


