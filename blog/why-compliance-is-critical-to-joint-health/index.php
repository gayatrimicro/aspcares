                                            <p class="product_desc">
                                            Hemophilia is a rare, genetically linked bleeding disorder in which blood does not clot normally. It is caused by a deficiency in clotting factors. <a href="../what-is-factor-VIII/" target="_blank">(Usually factor VIII or factor IX.)</a></p>
  <p class="product_desc">                                          
Hemophilia is characterized by excessive bleeding episodes, often involving bleeding into joints and soft tissues such as muscles. Over time, repeated hemarthrosis (bleeding into joints), can result in significant joint damage.

                                            </p>

                                          <p class="product_desc"><h2 class="find htwo">
                                           Hemophilia Joint Bleeds
                                            </h2></p>
                                            <p class="product_desc">
                                            Although it can affect any joint in the body, certain joints, are more vulnerable to damage. Ankles are often the first and most severely affected. Elbow and knees are also prone to accumulated damage. Studies show that as many as half of all joint bleeds occur in the knees.</p>
 <p class="product_desc">
Bleeding into a joint typically begins with a small break or tear in the synovial membrane. (The synovial membrane is a specialized form of connective tissue which lines the inner surface of joints.)

                                            </p>

 <p class="product_desc"><b class="find">
                                          What Causes Joint Bleeds?
                                            </b><br>
                                          Joint bleeds can be caused by trauma, although many have no obvious cause. Even routine activities and or small bumps can cause minute tears in the synovial membrane and or damage small blood vessels, causing blood to fill the joint cavity. Healthy people who do not have hemophilia, rarely notice any difference in their joints from these kinds of micro-injuries.</p>

                                           

                                             <p class="product_desc"><b class="find">
                                           Tiny Tears. Big Bleeds
                                            </b><br>
                                            In hemophilia patients however, even tiny abrasions and tears can lead to exaggerated bleeding into the synovial spaces of the joint. (The synovial space is formed by a flexible, thick membrane, filled with a fluid that lubricates the joint.)</p>
                                             <p class="product_desc">
                                            Because the space is extremely limited, any large buildup of fluid or blood can cause severe pain, swelling, and damage to the joint and surrounding tissues. Over time, repeated bleeding episodes can cause chronic pain, or even deformity in the affected joint. <a href="../managing-hemophilia/" target="_blank"> Click here</a>.</p>
 <p class="product_desc">
Studies have consistently shown that prophylactic (preventative) factor replacement therapy using plasma derived or recombinant clotting factors helps to reduce the chance of damage caused by these kinds of joint bleeds.</p>

 <p class="product_desc"><b class="find">
                                           Preventative Factor Replacement is key to Joint Health
                                            </b><br>
                                            Like a keystone , joint health is held in place on either side by a combination of preventative clotting factor treatments and personalized monitoring of the individual joints.</p>
<p class="product_desc">
Over time, patients and their treatment teams usually come to understand what kinds of activities typically trigger a bleed, the current health of the joint(s) in question, and the effectiveness and timing of factor replacement therapy.
</p>  

 <p class="product_desc"><b class="find">
                                          Hemophilia Joint Damage Can Take a Decade or More to Show
                                            </b><br>
                                          According to the<span style="font-style: italic;
"> Hemophilia Joint Health Score Reliability Study</span>, the degree of joint damage can be difficult to ascertain because it often occurs very slowly. Even imaging studies may only display subtle changes over time.</p>
 <p class="product_desc">
This is why the Hemophilia Joint Health Score was developed. The score helps providers assess joint health and impairment by measuring range of motion, flexion, gait, and other factors.
</p>
<p class="product_desc">
Other tests, including imaging studies and joint aspiration (which involves removing synovial fluid from a joint), can also help healthcare providers determine the health of affected joints. If you, or your child has hemophilia, your hemophilia medical care provider may recommend a joint protocol to help you manage bleeding episodes and reduce any potential complications from joint damage.</p><br>        
                                                                             
                                            
                                             

