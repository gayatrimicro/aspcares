<p class="product_desc">
Women who carry the gene that causes hemophilia on their X chromosome are known as carriers. While a woman can carry the faulty gene, it is the male’s sperm that determines the sex of the child. </p>
<p class="product_desc">
If a man has hemophilia, he cannot pass it down to his sons, but he can pass the trait down to his daughters. If a woman is a carrier, her sons have a 50% chance of having hemophilia. If she has a daughter, she will have a 50% chance of being a carrier as well. 
</p>

<p class="product_desc"><b class="find">Do women who carry the gene for hemophilia have symptoms?</b><br>Women who are carriers can have symptoms. These may include heavier menstruation and or excessive bleeding after giving birth. </p>


<p class="product_desc"><b class="find">What is Christmas disease? </b><br>
Hemophilia B is sometimes referred to as Christmas disease, after the first patient, Stephen Christmas was diagnosed with this form of hemophilia in 1952.  </p>


<p class="product_desc"><b class="find">How Are Clotting Factors Named?</b><br>
  Clotting factors are named in the order of their discovery rather than where they occur in the process of clotting. They are always expressed as Roman numerals. </p>

  <p class="product_desc"><b class="find">How does the percentage of clotting factors relate to severity and bleeding episodes?</b><br>
  Regardless of which factor is missing, the severity of the hemophilia depends on how much of a given clotting factor is present in the blood. People who do not have hemophilia have 100% of all clotting factors. </p>

<p class="product_desc"><b class="find">
Lower clotting factor percentages = more severe symptoms</b><br>
In those with hemophilia, the percentage can range from almost none (1% or less), to 30% of normal levels. The lower the clotting factor percentage, the more severe the symptoms.</p>
<p class="product_desc">
People with 1% or less of a clotting factor for example, can bleed without an injury. They may also experience a bleeding episode as much as one to two times a week. Bleeding episodes also frequently involve bleeding in muscles and or joints. </p>
<p class="product_desc">
People with 1% to 5%, can bleed with a slight injury, and may have a bleeding episode once a month or so. They may also experience bleeds which involve joints.</p>
<p class="product_desc"> 
People with mild hemophilia on the other hand, who have 6% to 25% of a clotting factor, usually only experience bleeds with severe injuries or after surgery. They may never experience an uncontrolled bleeding episode. If they do, it rarely involves joints. 
</p>
<p class="product_desc"><b class="find"> When is Hemophilia Considered Mild, Moderate and Severe?</b><br>
Mild hemophilia occurs when patients only have 6% to 30% of a specific clotting factor. Moderate hemophilia represents factor levels between 1% to 5%. Clotting factor levels below 1% are considered very severe. </p>
<p class="product_desc">
Approximately 60% of people diagnosed with hemophilia are considered severe. Approximately 15% of causes are classified as moderate. The remaining 25% are considered mild. Bear in mind that even mild forms of hemophilia can be life-threatening if bleeding is not properly managed.
</p>
<p class="product_desc"><b class="find">What causes bleeding episodes? </b><br>
Depending on the severity, bleeding episodes may be triggered by obvious injuries such as scrapes, cuts and bruises, surgical procedures, or they may occur spontaneously within the body in areas such as muscles, under the skin, and into the joints. 

 </p>
<p class="product_desc"><b class="find">
What is the main form of treatment for missing clotting factors (hemophilia)? 

</b><br>
The main form of treatment involves what is known as replacement therapy. <a href="../all-about-hemophilia-replacement-therapy/" class="redunder">Replacement therapy</a> involves replacing missing clotting factors.</p>

<p class="product_desc"><b class="find">What are comorbidities? </b><br>
Comorbidities is a term that means more than one chronic condition or disease is present in the same patient. For instance, a person with a bleeding disorder may also have a heart disease, hepatitis, or liver disease. These are considered comorbidities. </p>
