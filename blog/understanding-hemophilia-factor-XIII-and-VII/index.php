</p>

<p class="product_desc"><b class="find">How is Hemophilia diagnosed? </b><br>
<a href="../hemophilia-diagnosis/" class="redunder">Hemophilia is usually diagnosed</a> very early in childhood when it is moderate to severe. Newborns showing symptoms of hemophilia, or those with a family history of the disease are often tested early. </p>
<p class="product_desc">
Milder forms may not be diagnosed until adulthood. Diagnosis usually follows an excessive bleeding incident which might stem from a surgery or trauma. Blood tests, called blood factor assays, can determine if clotting factors are at or below normal levels. 
</p> 

 
<p class="product_desc"><b class="find">What are the average age(s) of diagnosis? </b><br>
The average age of diagnosis for those with mild hemophilia is 36 months of age. The average age for those with moderate hemophilia is 8 months old. Severe hemophilia is generally diagnosed by one month of age. </p>


<p class="product_desc"><b class="find">If a clotting factor is missing, does it form only a partial clot?</b> <br>
No. Clotting, or coagulation, is actually quite complex. Each step must be completed in a specific order, or the entire process (called a clotting cascade), stops because each step activates the next. People who are missing clotting factors such as XIII or VIII do not clot in the same way that others without a bleeding disorder do. </p> 

<p class="product_desc"><b class="find">
How does blood normally clot?</b><br> 
When a blood vessel is injured, certain types of blood cells, called platelets, mesh with specialized proteins within blood plasma (the liquid part of blood) to form a clot where the damage occurred. This process takes a small amount of blood from its usual liquid form to a soft gel form, creating a temporary plug.  From there it becomes a strong fibrin clot, completely sealing the damaged area of the blood vessel.   </p>
<p class="product_desc"><b class="find">Three stages of clotting</b><br>
The clotting process occurs in three stages. The first stage begins when the affected blood vessel spasms or constricts to limit blood loss. The second stage involves the formation of a temporary plug as platelets begin to clump together to seal off the flow of blood.</p>
 <p class="product_desc">
The third stage is the creation of a fibrin clot. A fibrin clot is much like a living mesh of fibers which traps more platelets and other blood cells to create an even stronger clot. 
</p>
<p class="product_desc"><b class="find">What happens when the clot is no longer needed?
</b><br>
Once a clot is no longer needed, the body breaks it down and removes it. </p>

<p class="product_desc"><b class="find">Can girls have hemophilia?</b><br>
  Although girls can have hemophilia, it is much rarer and usually has milder symptoms. It can still be serious, or even life-threatening though, if appropriate medical treatment is not provided. </p>
  <p class="product_desc">
Symptoms in females typically include heavy menstrual bleeding, abnormal bleeding following childbirth, easy bruising, prolonged bleeding after surgery or trauma, and or following dental procedures.  </p>

<p class="product_desc"><b class="find">What does it mean for a woman to be a carrier?</b><br>
Hemophilia is what is known as a recessive genetic disorder that is carried on the X chromosome. (The X and Y chromosomes are responsible for gender.) Women typically have two of the X chromosomes and men have one X and one Y. </p>
<br>
                                                                             
