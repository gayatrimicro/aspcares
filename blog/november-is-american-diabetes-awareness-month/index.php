<p class="product_desc">
  <h2 class="find htwo left">What is Type 1 diabetes?</h2></p>
  <p class="product_desc">Unlike type 2 diabetes, which tends to develop over time, type 1 diabetes can occur suddenly. Type 1 diabetes occurs when the body’s immune system attacks and destroys the cells which produce insulin.</p> 

<p class="product_desc">
Type 1 diabetes may have a genetic link. Many researchers also believe it might be triggered by a virus or in response to environmental factors. Type 1 diabetes is sometimes called juvenile diabetes, because it usually starts during adolescence.
</p>


<p class="product_desc">
  <h2 class="find htwo left">
What is Gestational Diabetes</h2></p>
<p class="product_desc">
Gestational diabetes is a form of diabetes that only develops during pregnancy. It is believed that the hormonal changes which occur during pregnancy make the body less capable of using and regulation insulin. Obesity, activity levels, and inherited traits may also play a role in developing gestational diabetes. </p>

<p class="product_desc">
In most cases, gestational diabetes resolves after the baby is born. Even so, if you developed gestational diabetes during your pregnancy, your risk of becoming diabetic becomes much higher. Half of all women who get gestational diabetes will go on to develop type 2 diabetes later in life. 

</p>

<p class="product_desc">
  <h2 class="find htwo left">
Children Carried During Pregnancies Involving Gestational Diabetes Have a Higher Risk of Developing Type 2 Diabetes</h2></p>
<p class="product_desc">
Moreover, any children that you carry when you got gestational diabetes, will also have a higher risk of developing type 2 diabetes at some point in their lifetime. You can reduce the risk of developing type 2 diabetes for yourself and your child by eating a healthy diet, being and staying physically active, and managing your blood sugar levels. </p>
<p class="product_desc">
Your healthcare provider can discuss the best ways to control your blood sugar levels. They can also determine if you need insulin or other medications to help you stay within a healthy blood glucose range. </p>


