 <p class="product_desc">  
 November has been designed as National Diabetes month by the American Diabetes Association (ADA). This year, in addition to raising awareness about diabetes, the ADA has chosen to focus extra attention on gestational diabetes, a type of diabetes that develops during pregnancy. 
</p>
<p class="product_desc">  
You can learn more about gestational diabetes later in this blog. But first, you should understand more about what diabetes is, how it can affect your health and or the health of someone you care about. 
 </p>
 
 
                                    
 <p class="product_desc">
  <h2 class="find htwo">100 Million Americans have Diabetes or Prediabetes</h2></p>
  <p class="product_desc">
According to a report by the Center of Disease Control (CDC), as of 2015, over 100 million people in America are living with either diabetes or prediabetes. (Prediabetes is a reversible condition in which blood sugar levels are higher than normal, but not high enough to be considered diabetes.) </p>
<p class="product_desc">
The report also states that an estimated 30.3 million American, or 9.4% of the total U.S. population, have diabetes. Of those, 23.1 million people have been diagnosed, and another 7.2 million have not been diagnosed, but still have the disease. By far, type 2 diabetes is the most common form of diabetes, accounting for 90%- 95% of all cases. </p>



<p class="product_desc">
  <h2 class="find htwo">What is Diabetes?</h2></p>
<p class="product_desc">Diabetes is a condition is which blood glucose (blood sugar) levels become too high. This is important because your body uses blood glucose as fuel. To convert blood sugar to a usable form though, the body must make a hormone known as insulin. Insulin works like a shuttle, moving glucose into cells where it is converted into energy, among other functions.  </p>

<p class="product_desc">
In a healthy person who does not have diabetes, glucose is broken out during the digestive process, where it is then released into the bloodstream. When glucose levels rise, <a href="../november-is-pancreatic-awareness-month/" target="_blank">the pancreas</a> begins to secrete insulin. 

</p>
<p class="product_desc">
  When insulin is released, it sends signals to cells, causing them to start taking in glucose, which is used to fuel cellular processes. (Any excess blood glucose in the cells is converted into glycogen, which is then stored in the liver and muscles or later use.)</p>

<p class="product_desc">
  When someone is diabetic, they either do not make enough insulin, or the body does not use it in the way that it should. This causes too much glucose to stay in the blood. </p>



<p class="product_desc">
  <h2 class="find htwo">Normal Glucose Ranges</h2></p>
 <p class="product_desc">Normal blood glucose levels are typically less than 100 mg/dL (dL stands for milligrams per deciliter) when you have not eaten for at least eight hours, or less than 140 mg/dL within two hours of eating.  </p>
 <p class="product_desc">
Your blood sugar levels naturally drop to around 70-80 mg/dL before eating. It should be noted that in some cases, your normal blood sugar level may be as low as 60 mg/dL, or as high as 90 mg/dL, yet still be considered within normal ranges. 
</p>
    
  <p class="product_desc"> Over time, excess blood glucose levels can cause serious damage to organs, tissues, nerves, and blood vessels. Left unmanaged, diabetes can lead to health issues, such as:</p>
<p class="product_desc">
● Heart disease and Heart Attacks <br>
● Problems with your eyes which can lead to loss of vision <br>
● Stroke<br>
● Kidney disease or kidney failure<br>
● Dental Disease <br>
● Nerve Damage such as neuropathy, causing pain, numbness and tingling which may be severe<br>
● Problems with you feet such as nerve damage, poor circulation and wounds that may not heal well <br>
● Decreased wound healing which can lead to amputation of the feet and or legs in severe cases
 </p>

 <p class="product_desc">
  <h2 class="find htwo left">Symptoms of Diabetes</h2></p>
  <p class="product_desc">
  The symptoms of diabetes can include:</p>
 <p class="product_desc">
● Feeling thirsty more than usual<br>
● Needing to urinate frequently <br>
● Feeling tired<br>
● Having blurred vision<br>
● Developing sores that do not heal<br>
● Losing weight without trying<br>
● Numbness or tingling in your hands or feet
</p>

