<p class="product_desc"><a href="../hiv-treatment-and-prevention/" class="redunder">HIV</a>, or human immunodeficiency virus is a virus that attacks the immune system. The most commonly prescribed HIV medications are what are known as antivirals. Antiviral therapy (ART) is recommended for all HIV positive patients. </p>
<p class="product_desc">Generally speaking, HIV positive patients are treated with three HIV medications from two main classes of drugs. While these medications do not cure HIV per se, they can lower the viral load (number of active viruses in the patient’s body), reduce the risk of transmitting the virus to someone else, and help patients live longer.  
</p>
                                        
                                        
                                      
                                    
                                        <p class="product_desc"><b class="find">
                                            How the HIV Virus Creates Copies of Itself 
                                            </b><br>
                                          HIV, like all viruses cannot replicate (multiply) without the components of living host cells. By hijacking specific cellular processes, they take over host cells, forcing them to make copies of the virus instead of normal healthy cells. Once new copies of the virus ready, they are released. They then move on to infect other cells and the process begins again. 

                                            </p>



                                             <p class="product_desc"><b class="find">
                                           HIV is a Retrovirus 
                                            </b><br>
                                          HIV is somewhat different in that it is what is known as a retrovirus. This means that it injects its own genetic material into cells. To do this, it uses an enzyme called reverse transcriptase, which translates viral RNA into DNA. </p>
<p class="product_desc">Antiviral HIV medications use slightly different mechanisms to block this, and other cellular processes to prevent the virus from creating duplicates of itself at different stages in its life cycle.


                                            </p>

<p class="product_desc"><h2 class="find htwo left">
Major HIV Medication Classes and How They Work</h2></p>
<p class="product_desc">
There are six major kinds, or classes, of HIV medications that are currently prescribed for HIV patients. These include:</p>

<p class="product_desc"><b class="find">Nucleoside or Nucleotide Reverse Transcriptase Inhibitors) (NRTIs) </b><br>
NRTIs and NNRTIs use slightly different ways to block an enzyme known as reverse transcriptase. Reverse transcriptase works like a translator, converting the virus’s RNA to DNA, a form that human cells can “read” use as instructions. </p>
<p class="product_desc"><b class="find">Integrase Inhibitors</b> <br>
HIV uses an enzyme called integrase to deploy its viral DNA payload into the host cell. Integrase inhibitors block this process, preventing the HIV virus from creating copies before the process can proceed.</p>
<p class="product_desc"><b class="find">Protease Inhibitors (PIs)</b> <br>
Protease is an enzyme that breaks down cellular proteins and peptides (chains of amino acids), unlocking key components that HIV needs to bind to create new active copies. Protease inhibitors then, work by preventing the virus from using this key enzyme. </p> 
<p class="product_desc"><b class="find">Fusion Inhibitors </b><br>
Fusion Inhibitors are a type of HIV medication that works outside the cell. It creates a protective barrier by locking onto the outside ‘envelope” of a cell, preventing the virus from gaining entrance into the inside of the cell.  </p>
<p class="product_desc"><b class="find">Entry Inhibitors</b><br>
Entry inhibitors also work by “locking out” the HIV virus, keeping it out of the cell so that it cannot gain access to the cellular components it needs to create duplicates of itself. </p>



