</p>

          <p class="product_desc"><b class="find">HIV Can Cause Overlapping Symptoms </b><br>
          Besides HIV medication side effects, HIV positive patients should be aware that the HIV virus itself can cause certain symptoms. Your healthcare provider can determine the difference and provide medical support. Always inform your healthcare provider of any unusual, severe, or prolonged side effects.</p> 
 <p class="product_desc">If you do experience side effects, make sure that you do not skip or reduce medication doses, or stop taking them without the advice of a qualified healthcare provider. <b>Stopping you prescribed medications can increase the risk of developing a drug resistance. </b> 

</p>

<p class="product_desc">
  
  <b class="find">What is HIV Drug Resistance?</b><br>
  HIV drug resistance occurs when the HIV virus becomes tolerant, or resistant to, a medication, allowing it to multiply even in the presence of the medication. In the case of the HIV virus, this means that it develops new mutations, that allow it to bypass the specific pathways that a given medication inhibits or blocks. </p>
<p class="product_desc">In other words, HIV can adapt to its environment. HIV drug resistance can occur when patients do not take enough of their prescribed HIV drugs, or when they take them for long periods of time and the virus has a chance to adapt. 

</p>

