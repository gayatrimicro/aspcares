<p class="product_desc"><h2 class="find htwo left">HIV Medication Side Effects </h2></p>

<p class="product_desc">
  
  HIV medications can help HIV positive patients live longer, healthier lives. Like all medications though, they can also cause unwanted side effects. Most side effects are moderate and can be managed, some however, can be severe.</p> 
<p class="product_desc">Different patients taking the same medication may also experience side effects in different ways, or to different degrees. Overall, the benefits of HIV medications far outweigh the risks of HIV medication side effects. Be sure to talk to your healthcare provider about any side effects you may experience.

</p>

  <p class="product_desc"><b class="find">Short Term Side Effects </b><br>
  During the first few weeks of treatment, some patients experience short term side effects such as:</p>
  <p class="product_desc">
• Fatigue<br>
• Nausea and or vomiting <br>
• Diarrhea<br>
• Fever<br>
• Muscle aches and or soreness<br>
• Dizziness or lightheadedness<br>
• Trouble sleeping<br>
• Headaches</p>


                                            
