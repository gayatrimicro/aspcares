                                        <p class="product_desc">
                                       The human immunodeficiency virus, or HIV, is not yet curable, but it is still very treatable. People who are HIV positive can live long and productive lives thanks in large part to adhering to an effective HIV treatment plan. 
                                     </p>
  

 
 <p class="product_desc">  
  <h2 class="find htwo">Why Antiretrovirals Are Critical to HIV Treatment</h2></p>
  <p class="product_desc">
HIV medication regimes typically included specialized medications known as antiretrovirals. Unlike bacteria which can divide and reproduce on their own, viruses must insert their genetic material (in the form of DNA or RNA) into infected host cells to reproduce. </p>
 <p class="product_desc">
Once delivered, these genetic blueprints tell the host cells how to create the specific proteins the virus needs to create copies of itself. 


 </p>
                                    
 <p class="product_desc">
  <b class="find">HIV is a Retrovirus </b><br>
The HIV virus is different in several ways from other kinds of viruses. First, HIV is what is known as a retrovirus. This means that it requires extra steps to make copies of itself. Using an enzyme called reverse transcriptase, the HIV virus must first translate its viral RNA into a form of DNA that human cells can use. </p>
<p class="product_desc">
HIV medications exploit this as an Achilles’ heel, turning the tables on the virus by disrupting its ability to make copies of itself at critical times in its life cycle. 

</p>
    
  <p class="product_desc"><h2 class="find htwo left"> Most Common HIV Treatment Plan Medications</h2></p>
<p class="product_desc">The most effective HIV treatment plans include a mixture of different classes of antiviral medications. Known as a cocktail, targeted HIV medications are taken in specific combinations to fight the HIV virus. Each HIV drug works in a different way, interfering somewhere within the copying process, so that the virus cannot spread throughout the body. 

