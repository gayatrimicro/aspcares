                                            </b><br>Once a personal injury attorney has accepted your case, they will often write what is known as an LOP, or letter of protection. A form of promissory payment, it is basically a request for credit. A LOP allows you to see a seek medical attention while you wait for the legal matters to be worked out.</p>

